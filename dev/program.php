        <h4>Description</h4>


        <p>The Residency, “Outskirts of Urban Blue Print” is a 17-day program based in Palermo and is designed to accommodate a collective of 15 participants whose creative art, design and architectural interest reside in the evolutions of community within a diverse cultural landscape. There will be studio space and each participant will have some basic tools from which to produce work. The residency is designed to use the city as a vital laboratory to experience culture, collect and produce work culminating in an exhibition in the last three days of our stay as a part of Manifesta12. Participants will visit and interact with various cultural community organizations such as Codifas, Shared Urban Vegetable Garden for Social Integration, Moltivolti, Multi-Ethnic Restaurant & Cultural Meeting Place, just to mentioned a couple, as well as explore from the “street” the complex social, cultural, historical layers within the modern city of Palermo. Our accommodation will be in a small hotel right in the center of Palermo. There will be walking tours with a contemporary Art Historians Specializing in Outsider Art (Street Art) and Architecture and Design, guiding us through historically significant churches and museums with magnificent Byzantine Mosaics and Arab/Norman architecture. We will visit the Archeological Museum A. Salina, which has one of the richest archaeological collections in Italy, Ethnographic Museum G.Pitre’ and Palazzo Abatellis to view the renowned master piece “L’ Annunciata” (c. 1476) by the early Renaissances Sicilian painter Antonello da Messina.</p>

        <h4>Brief History</h4>

        <p>Since 1500 B.C., Sicily has seen a multitude of different cultures and populations, evidence of which can be seen in its architecture. Once a powerful colony of Magna Graecia, the island subsequently witnessed the various styles of its dominations from the Roman Empire to the Arabs and Normans, all leaving a range of styles from Greek and Roman to the baroque and 19th and 20th century architecture. It has been for centuries a crossroads of cultures and religions, the epitome of diversity.</p>

          <p>Palermo founded by the Phoenicians in the VII century BC and called Ziz, meaning flower, it was subsequently conquered by the Romans on Sicily who gave it the name Panormus, coming originally from the Greek meaning ‘big port’. Later the Arab occupation renamed it Balharm which more or less brings us to the present day Palermo. During the Arab occupation, Palermo was one of the principal Islamic center in the western world a significant piece of Sicilian and Italian history. </p>
        <p> In 1072 the city fell to the hands of the Norman. All the merchants and artisans and the Muslim population were allowed to continue their activities and professions in a normal way which is why, today, we still have examples of some of the best Arab/Norman decorative architecture in Sicily and the Mediterranean. After a brief period of decadence, Palermo and Sicily passed into the hands of Frederick II of Swabia (1212) and the city once more became a powerful center. Other invaders such as the French Anjous, the Spanish and then in the 1700s the Bourbons from Naples who built the baroque buildings in the city, all added their own personal touches to the architecture and culture.</p>

        <h4>About Manifesta 12 & OMA Architecture</h4>

        <p>The Residency Project “Outskirts of Urban Blue Print” runs parallel to and is associated with Art Biennial Manifesta 12. Manifesta, the nomadic European Contemporary Art Biennial, originated in the early nineties in response to the political, economic and social changes following the end of the Cold War and the subsequent steps towards European integration. Since then, Manifesta has developed into a travelling platform that focuses on the dialogue between art and society in Europe. Manifesta is a project based on community: its success depends on the collaboration between the international and local actors and the involvement and engagement of the local communities. The City of Palermo was important for Manifesta’s selection board for its representation of two important themes that identify contemporary Europe: migration and climate changes and how these issues impact our cities. Manifesta is the go-to place to discover emerging artists, thought-provoking ideas and new artworks in dialogue with spectacular location of every host city.</p>

          <p>OMA was founded by Rem Koolhaas and is an international practice operating within the tradition al boundaries of architecture and urbanism. OMA unveils “Palermo Atlas”: an interdisciplinary urban study of Palermo commissioned by Manifesta 12.</p>

          <p>The content of Manifesta 12 Palermo Atlas as articulated by Ippolito Pestellini Laparelli of OMA and the work takes its starting point from the awareness that there is no fixed way to approach or define Palermo. “The city cannot be reduced to a single statement or to a precise definition. It is rather a complex mosaic of fragments and identities emerging out of centuries of encounters and exchanges between civilizations. Its material archeology, cultural legacy, somatic traits and ecosystems are the tangible evidences of a long lasting syncretism. Today, the city can be considered an archipelago of the global: not a globalized city per se, but rather an incubator of different global conditions.</p>
